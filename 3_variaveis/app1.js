// variáveis em javascript => são maneiras de armazenar informações para usarmos posteriormente

// TIPO DE VARIÁVEL NOME DA VARIÁVEL VALOR

// VAR => maneira antiga variável dentro do javascript
// permite que adicionamos um valor nela e posteriormente possamos alterar
var nome = "Eduardo";
var nome = "Paulo";
console.log(`Meu nome é: ${nome}!`);

// LET => adicionar um valor nele e também podemos alterar esse valor
let nomeLet = "Eduardo";
nomeLet = "Pedro";
console.log(`Meu nome é: ${nomeLet}!`);

// CONST => adicionamos um valor e não podemos mais substituir esse valor
const nomeConst = "Eduardo";
console.log(`Meu nome é: ${nomeConst}!`);
