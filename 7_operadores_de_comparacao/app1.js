// igual ==
console.log("IGUAL ==");
console.log("10 == 10:", 10 == 10);
console.log("11 == 10:", 11 == 10);
console.log("a == a:", "a" == "a");
// javascript é case sensitive
console.log("a == A:", "a" == "A");

console.log("================================================");

// estritamente igual ===
console.log("ESTRITAMENTE IGUAL ===");
console.log("10 === 10", 10 === 10);
console.log("10 === 10", "10" === 10);

console.log("================================================");

// diferente !=
console.log("DIFERENTE !=");
console.log("10 != 10:", 10 != 10);
console.log("11 != 10:", 11 != 10);
console.log("a != a:", "a" != "a");
// javascript é case sensitive
console.log("a != A:", "a" != "A");

console.log("================================================");

// estritamente diferente !==
console.log("ESTRITAMENTE DIFERENTE !==");
console.log("10 !== 10", 10 !== 10);
console.log("10 !== 10", "10" !== 10);

console.log("================================================");

// operador maior >
console.log("OPERADOR MAIOR >");
console.log("10 > 11", 10 > 11);
console.log("11 > 10", 11 > 10);
console.log("10 > 10", 10 > 10);

console.log("================================================");

// operador menor <
console.log("OPERADOR MAIOR <");
console.log("10 < 11", 10 < 11);
console.log("11 < 10", 11 < 10);
console.log("10 < 10", 10 < 10);

console.log("================================================");

// operador maior >=
console.log("OPERADOR MAIOR OU IGUAL >=");
console.log("10 >= 11", 10 >= 11);
console.log("11 >= 10", 11 >= 10);
console.log("10 >= 10", 10 >= 10);

console.log("================================================");

// operador menor >=
console.log("OPERADOR MENOR OU IGUAL <=");
console.log("10 <= 11", 10 <= 11);
console.log("11 <= 10", 11 <= 10);
console.log("10 <= 10", 10 <= 10);

